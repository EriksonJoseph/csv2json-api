@router.get("/")
@tracker.measure_async_time
async def get_all_users(page: int = Query(1, ge=1), limit: int = Query(10, ge=1, le=100)):
  # ‡πÄ‡∏ä‡∏∑‡πà‡∏≠‡∏°‡∏ï‡πà‡∏≠‡∏Å‡∏±‡∏ö collection users
  users_collection = await get_collection("users")
  # ‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì skip ‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö pagination
  skip = (page - 1) * limit
  
  # ‡∏ô‡∏±‡∏ö‡∏à‡∏≥‡∏ô‡∏ß‡∏ô users ‡∏ó‡∏±‡πâ‡∏á‡∏´‡∏°‡∏î (‡πÉ‡∏ä‡πâ await ‡∏Å‡∏±‡∏ö Motor)
  total_users = await users_collection.count_documents({})
  
  # ‡∏î‡∏∂‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÇ‡∏î‡∏¢‡∏°‡∏µ‡∏Å‡∏≤‡∏£‡∏ó‡∏≥ pagiantion
  cursor = users_collection.find().skip(skip).limit(limit)
  users_list = await cursor.to_list(length=limit)
  users = list_serial(users_list)

  # ‡∏™‡πà‡∏á‡∏Ñ‡∏∑‡∏ô‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏û‡∏£‡πâ‡∏≠‡∏° metadata ‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö pagination
  return {
    "message": "üë§ ‡∏£‡∏≤‡∏¢‡∏ä‡∏∑‡πà‡∏≠‡∏ú‡∏π‡πâ‡πÉ‡∏ä‡πâ‡∏ó‡∏±‡πâ‡∏á‡∏´‡∏°‡∏î",
    "total": total_users,
    "page": page,
    "limit": limit,
    "pages": (total_users + limit - 1) // limit,
    "users": users
  }

